<p>投資顧問とは？</p>　

<p>有価証券への投資について専門家の視点からの分析を行い、
分析の結果導き出された投資戦略を一般の投資家にアドバイスとして提供する業者のことを指します。
長期の投資に強い投資顧問会社、デイトレードに興味のある投資顧問会社などそれぞれの会社が得意分野を持っている場合が多いです。
過去に個人投資家を相手に投資顧問会社が詐欺行為を行った経緯から、
現在では投資顧問業法に基づいた登録が必要となっています。</p>

<p>投資顧問会社は有価証券の投資についての分析やアドバイスの提供を行うことのみが業務内容であるが、
これに加えて投資家から投資についての判断の全部または一部を任される契約のことを「投資一任契約」と呼ぶ。
このような投資一任業者については、監督官庁の認可を受ける必要がある。
近年のテクノロジーの発達によって、パソコンや携帯電話のメールなどを利用して活発に情報の提供を行う会社が増えている。
ただし、投資顧問会社のアドバイスに従ったり、投資顧問会社に投資を一任したりして損失を被った場合には自己責任となるので、
投資顧問会社の選択は注意深く進める必要があります。</p>
